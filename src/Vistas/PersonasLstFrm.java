    private void EliminarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarBtnActionPerformed
        int idx=personaLstTbl.getSelectedRow();
        Persona p =listaPersona.get(idx);
        //JOptionPane.showMessageDialog(null, p.toString());
        PersonaDAO perdao=new PersonaDAO();
        int rta=JOptionPane.showConfirmDialog(null, "Desea eliminar el registro?",
                "Eliminar Registro Persona", JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.INFORMATION_MESSAGE);
        if (rta == 0) {
            try {
                perdao.eliminarPersona(p);
                JOptionPane.showMessageDialog(null, "Operación realizada");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Operación No Realizada");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Operación No Realizada");
        }
    }//GEN-LAST:event_EliminarBtnActionPerformed

