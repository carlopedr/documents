    private void cargarDatosBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargarDatosBtnActionPerformed
        // TODO add your handling code here:
        PersonaDAO perdao=new PersonaDAO();
        Organizacion org = new Organizacion();
        //Se traen los datos de la BD
        listaPersona=(ArrayList<Persona>) perdao.listPersona();
        //Se ordena el lstado de personas
        Collections.sort(listaPersona, new Comparator<Persona>() {
            @Override
            public int compare(Persona o1, Persona o2) {
                return o1.getNombre().compareToIgnoreCase(o2.getNombre());
            }
        });
        //Código para insertar los datos en JTable
        Object[] datos = new Object[dtmPersonas.getColumnCount()];
        int i=1;
        dtmPersonas.setRowCount(0);
        for (Persona persona : listaPersona) {
            datos[0] = persona.getId();
            datos[1] = persona.getNombre();
            datos[2] = persona.getEmail();
            datos[3] = persona.getDireccion();
            datos[4] = persona.getFechaNacimiento();
            try {
                org = persona.getEps();
                datos[5] = org.getNombre();
            }
            catch (Exception e){
                datos[5] = "";
            }
            i++;
            dtmPersonas.addRow(datos);            
        }
        personaLstTbl.setModel(dtmPersonas);
    }//GEN-LAST:event_cargarDatosBtnActionPerformed

